export interface InputProps {
  /**
   * 글자 색상
   */
  color?: string;
  /**
   * 크기 설정
   */
  size?: "small" | "medium" | "large";
  /**
   * Placeholder 텍스트 입력
   */
  placeholder: string;
  /**
   * Placeholder 글자 색상
   */
  placeholderColor?: string;
  /**
   * 라인 색상
   */
  borderColor?: string;
}
